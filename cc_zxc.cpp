void yash()
{
	int n;
	cin >> n;
	string s;
	cin >> s;

	int diff = 0, res = 0;
	for (int i  = 1; i < n; i++) {
		if (s[i] != s[i - 1]) {
			diff++;
		} else {
			res++;
		}
	}

	int ans = (res * diff);
	diff--;

	ans += (diff * (diff + 1)) / 2;
	cout << ans << '\n';
}

