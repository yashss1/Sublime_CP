void yash()
{
	int n;
	cin >> n;

	int ans = INT_MAX;
	for (int i = 0; i < 32; i++) {
		int x = pow(2, i) - 1;
		if (x >= n) {
			ans = min(ans, x - n);
			continue;
		}

		for (int j = 0; j < 32; j++) {
			x *= 2;
			if (x >= n) {
				ans = min(ans, x - n);
				break;
			}
		}
	}

	cout << ans << '\n';
}

